package 학생이름_저장_및_검색;

public class Students {
	
	public String name;
	public String no;
	
	
	public Students(String name, String no) {
		this.name = name;
		this.no = no;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
	

}
